#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	len;

	len = 0;
	while (len < size && dst[len])
		len++;
	if (len == size)
		return (len + ft_strlen(src));
	i = 0;
	while (len + i + 1 < size && src[i])
	{
		dst[len + i] = src[i];
		i++;
	}
	dst[len + i] = '\0';
	return (len + ft_strlen(src));
}
