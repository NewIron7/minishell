#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*mem;

	i = 0;
	mem = s;
	while (i < n)
	{
		mem[i] = c;
		i++;
	}
	return (s);
}
